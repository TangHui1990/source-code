#define NFDATA (*(volatile unsigned char*)0x4E000010)
#define NFCONT (*(volatile unsigned long*)0x4E000004)
#define NFSTAT (*(volatile unsigned char*)0x4E000020)
#define NFCMMD (*(volatile unsigned char*)0x4E000008)
#define NFADDR (*(volatile unsigned char*)0x4E00000C)
#define NFCONF (*(volatile unsigned long*)0x4E000000)

#define TACLS 1 
#define TWRPH0 2
#define TWRPH1 1

/*选择芯片*/
void select_chip(void)
{
	NFCONT &= ~(1 << 1);
}

/*取消选中芯片*/
void deselect_chip(void)
{
	NFCONT |= (1 << 1);
}

/*清除RnB*/
void clear_RnB(void)
{
	NFSTAT |= (1 << 2);
}

/*发送命令*/
void send_cmd(unsigned cmd)
{
	NFCMMD = cmd;
}

/*发送地址*/
void send_addr(unsigned addr)
{
	NFADDR = addr;
}

/*等待RnB*/
void wait_RnB(void)
{
	while (!(NFSTAT&(1<<2)))
	{
		continue;
	}
}

void nand_reset(void)
{
	/*选中flash*/
	select_chip();

	/*清除RnB*/
	clear_RnB();

	/*发送0xff命令*/
	send_cmd(0xff);

	/*等待RnB*/
	wait_RnB();

	/*取消选中flash*/
	deselect_chip();
}

/*nand flash init*/
void nandflash_init(void)
{
	/*初始化NFCONF*/
	NFCONF = (TACLS<<12) | (TWRPH0<<8) | (TWRPH1<<4);

	/*初始化NFCONT*/
	NFCONT = (1<<0) | (1<<1);

	/*复位*/
	nand_reset();
}

/*nand flash read*/
void NF_PageRead(unsigned long addr, unsigned char *buff)
{
	int i;

	/*选中nandflash芯片*/
	select_chip();

	/*清除RnB*/
	clear_RnB();

	/*发送命令0x00*/
	send_cmd(0x00);

	/*发送列地址，2个周期*/
	send_addr(0x00); /*列地址就是页内偏移，按页读没有页内偏移，故为0*/
	send_addr(0x00);

	/*发送行地址, 3个周期*/
	send_addr(addr&0xff);
	send_addr((addr>>8)&0xff);
	send_addr((addr>>16)&0xff);

	/*发送命令0x30*/
	send_cmd(0x30);

	/*等待RnB*/
	wait_RnB();

	/*读取数据*/
	for ( i = 0; i < 2048; i++) /*一个页有2K的byte，所以是2048*/
	{
		buff[i] = NFDATA;
	}

	/*取消选中nnadflash芯片*/
	deselect_chip();
}

/*nand flash erase*/
int NF_Erase(unsigned long addr)
{
	int ret;

	/*选中flash芯片*/
	select_chip();

	/*清除RnB*/
	clear_RnB();

	/*发送命令0x60*/
	send_cmd(0x60);

	/*发送行地址*/
	send_addr(addr&0xff);
	send_addr((addr>>8)&0xff);
	send_addr((addr>>16)&0xff);

	/*发送命令D0*/
	send_cmd(0xD0);

	/*等待RnB*/
	wait_RnB();

	/*发送命令0x70*/
	send_cmd(0x70);

	/*读取擦除结果*/
	ret = NFDATA;

	/*取消选中flash芯片*/
	deselect_chip();

	return (ret);
}

/*nand flash write*/
int NF_WritePage(unsigned long addr, unsigned char *buff)
{
	unsigned int i, ret = 0;

	/*选中nandflash*/
	select_chip();

	/*清除RnB*/
	clear_RnB();

	/*发送0x80命令*/
	send_cmd(0x80);

	/*发送2个列地址*/
	send_addr(0x00);
	send_addr(0x00);

	/*发送3个行地址*/
	send_addr(addr&0xff);
	send_addr((addr>>8)&0xff);
	send_addr((addr>>16)*0xff);

	/*发送数据*/
	for (i = 0; i < 2048; i++)
	{
		NFDATA = buff[i];
	}

	/*发送0x10命令*/
	send_cmd(0x10);

	/*等待RnB*/
	wait_RnB();

	/*发送0x70命令*/
	send_cmd(0x70);

	/*读取写入结果*/
	ret = NFDATA;     /*0写入成功，1写入失败*/

	/*取消选中nandflash*/
	deselect_chip();

	return (ret);
}

/*将nand flash 里面的内容读取到内存*/
void nand_to_ram(unsigned long start_addr, unsigned char *sdram_addr, int size)
{
	int i;
	
	/*以页读，没有列地址，而列地址占地址的低11位，所以右移11*/
	for (i = (start_addr >> 11); size > 0; )
	{
		NF_PageRead(i, sdram_addr);
		size -= 2048;                   /*一页2K byte*/
		sdram_addr += 2048;
		i++;
	}
}
